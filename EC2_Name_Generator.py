# Import the secrets module for generating secure random strings
import secrets

# Initialize a counter variable for the while loop
i = 1

# Print a welcome message for the AWS EC2 Name Generator
print('''
============================================================
======================AWS EC2 NAME GENERATOR================
============================================================
''')

# Prompt the user to enter their department name
Department_Name = input('What department do you belong to? ')

# Check if the department name is one of the specified options
if Department_Name == 'Marketing' or Department_Name == 'Accounting' or Department_Name == 'FinOps':
    # Print a confirmation message
    print(Department_Name.capitalize(), ', Got it!')
    
    # Ask for the number of EC2 instances to name
    Ec2_Total = int(input('Enter the number of EC2 Instances '))

    # Loop through the number of instances and generate names
    while i <= Ec2_Total:
        # If the department is Marketing, generate a name with 'marketing' prefix
        if Department_Name == 'Marketing':
            print(Department_Name.lower() + secrets.token_urlsafe(8))
        # If the department is Accounting, generate a name with 'accounting' prefix
        elif Department_Name == 'Accounting':
            print(Department_Name.lower() + secrets.token_urlsafe(8))
        # If the department is FinOps, generate a name with 'finops' prefix
        # Note: This should be an elif statement for correct logic.
        else: Department_Name == 'FinOps'
        print(Department_Name.lower() + secrets.token_urlsafe(8))
        # Increment the counter
        i += 1
else:
    # If the department name is not recognized, deny access
    print('I am sorry, you do not have access to the name generator.')
